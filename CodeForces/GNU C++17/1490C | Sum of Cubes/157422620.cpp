void solve() {
    db n;
    cin >>n;
    if (n==1) {no; return;}
    for (db i=1; i*i*i<=n; i++) {
        db t=n-pow(i,3);
        t=cbrt(t);
        if (t>=1 and floor(t)==ceil(t)) {yes;return;}
    }
    no;
}
 
